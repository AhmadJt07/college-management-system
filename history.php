                        <p>As one of the earliest Government Colleges in Faisalabad, <b> Govt. Graduate College Samanabad Faisalabad </b> is a unique and well-reputed institute. It has the distinction of being the first Govt. College in Lyallpur since partition.
                            It appeared on the academic scene of the City as <b> Govt. Intermediate College Lyallpur </b> in <i> 1958 </i> when it was set in a rented building at People Colony, Lyallpur. Later, in <i> 1965 </i> it was shifted to its present <b> 117 kanal </b> vast purpose-built campus. The subsequent years saw the college reaching various significant milestones:
                            In <i> 1975, </i> it was upgraded to Degree level and in <i> 1999 </i> MA (Political Science) was started followed by English and Economics in <i> 2002 </i>and Islamic Studies in <i> 2010 </i>. Recognizing the importance of IT and commerce, the college introduced ICS and I.Com Programs in <i> 1999 </i> and <i> 2007 </i> respectively. The College is offering BS (FYDP) under a semester system in the disciplines of Botany, Chemistry, Geography, Computer Science, Economics, English, Information Technology, Islamic Studies, Mathematics, Physics, Sociology, Urdu, History, Punjabi, Statistics, Psychology, Zoology and Political Science. The College is affiliated with the <b> BISE Faisalabad </b>for intermediate and with <b> GC University Faisalabad </b> for higher studies. Heading a step forward in academics the College commenced its M.Phil in three disciplines i.e. Chemistry, Islamic Studies, and Urdu in spring
                            <i> 2014</i>. It affords a great pleasure that two batches of M.Phil have successfully completed their degrees. The college name was changed to <b> Govt. Graduate College Samanabad Faisalabad </b> in  <i>2021 </i> as per policy of HED, Government of PUNJAB.</p>
